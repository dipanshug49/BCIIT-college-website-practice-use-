  <h1>The Society</h1><br>
  <h2><b>Banarsidas Chandiwala Sewa Smarak Trust Society</b></h2>
<p>Banarsidas Chandiwala Sewa Smarak Trust Society was established 1952 by the dynamic and visionary Late Shri Brijkrishan Chandiwala, a veteran Gandhian and philanthropist, in the memory of his illustrious father Late Shri Banarsidas Chandiwala. President of prominent social service organisations such as Bharat Sewak Samaj and Sadachar Samiti, Shri Brijkrishan Chandiwala was a simple man, endowed with visionary mind. He formed the Society with the mission:</p>
                              <b style="padding-left: 150px;">“To Provide Yeomen Service in the Field of Health and Education.”</b><br>
<p>Banarsidas Chandiwala Sewa Smarak Trust Society is a registered society under the Societies Registration Act and is located at Chandiwala Estate, in the green belt of Kalkaji, New Delhi. To fulfil the mission, the Society provides services in health, education and sports in Delhi, viz.</p><br>
<h2>Janaki Devi Memorial College for Women</h2>
<p>This college was established in 1959 at Old Rajinder Nagar, New Delhi in affiliation with Delhi University. This college, besides imparting education in regular degree courses, provides number of vocational training programmes to the needy and the poor girls to prepare them to be self-employed.</p>
<h2>Banarsidas Chandiwala Institute of Medical Sciences (BCIMS)</h2>
<p>BCIMS located at Chandiwala Estate, Kalkaji, New Delhi is a major contribution of the Society towards the health care. Started as small dispensary in early 50s, today it is a multi-speciality hospital with research facilities. Various departments at BCIMS includes Eye, ENT, Dentistry, Orthopaedics Physiotherapy, Cardiology, Pathology, Radiology, Medicine, Surgery, Gynaecology, Paediatrics, Dermatology, Psychology, Homeopathy and Ayurvedic Dispensary with state-of-the-art facilities.</p>
<h2>Banarsidas Chandiwala Institute of Hotel Management Catering Technology (BCIHMCT)</h2>
<p>BCIHMCT was established in 1999 in the serene environment of Chandiwala Estate. It is affiliated with Guru Gobind Singh Indraprastha University (GGSIPU), Delhi and approved by the All India Council of Technical Education (AICTE). It the only Institute in Hotel Management Catering Technology in Delhi that runs AICTE approved four-year degree programme in HMCT. BCIHMCT provides state-of-the-art facilities to its students. Besides the degree level programmes it also runs industry focussed diploma level programmes approved by Board of Technical Education, Govt of NCT of Delhi.</p>
<h2>Banarsidas Chandiwala Institute of Physiotherapy (BCIP)</h2>
<p>BCIP was established in 2003 at Chandiwala Estate, New Delhi. It runs four-and-a-half year Bachelor in Physiotherapy (BPT) programme in affiliation with Guru Gobind Singh Indraprastha University (GGSIPU), Delhi and is approved by Govt of NCT of Delhi. The availability of hospital in the campus provides clinical experience.</p>
<h2>Banarsidas Chandiwala Sports Centre</h2>
<p>Chandiwala Estate at New Delhi has state-of-the-art air-conditioned Sports Centre that supports the students in developing their competitive skills and physical health. The Centre has international level facilities in Swimming, Lawn Tennis, Table Tennis, Badminton, Squash, Shooting, Boxing and a Gymnasium. The Society sponsors and organises every year “Banarsidas Chandiwala Cricket Tournament” for North India. Within the ambit of Sports Centre the Society runs Boxing Academy, where promising sportsman have been adopted. The Society bears the entire expenditure of education, living and their development into quality boxers.</p>
