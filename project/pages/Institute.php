  <h1>The Institute</h1><br>
  <p>Banarsidas Chandiwala Institute of Information Technology (BCIIT) was established in 1999 to run Three-Year Master of Computer Application (MCA) programme. The Institute is affiliated with Guru Gobind Singh Indraprastha University, Delhi for awarding the degree and is approved by the All India Council of Technical Education (AICTE), Ministry of HRD, Government of India.The Institute is established under the aegis of Sri Banarsidas Chandiwala Sewa Smarak Trust Society, a charitable Society working in the field of health and education since 1952.</p>

<P>The ‘Process Management Approach’ followed by the Institute facilitates in efficient and effective implementation of each process and it’s monitoring. Both the stakeholders – the faculty and the students – share the vision and mission of the Institute and are active partners in its implementation.</P>

<p>The Institute is student centric where the faculty members are always alive to the needs of the students and are focussed to develop them to top class software professionals. The Programme Coordinator and mentors monitor the progress of each student. The students go through continuous evaluation for which tests are conducted every Monday. For the personality development the students undergo communication skills, etiquette, and organisation behaviour classes. To develop their competitive skills the students are subjected to debates, quizzes, software development tests, sports and cultural programmes.</p>

<p>The Institute is located in the midst of the business centres of South Delhi, viz., Nehru Place and Okhla Industrial Town in the serene environment of Chandiwala Estate. The location makes a perfect fit from IT perspective with Nehru Place as hardware hub and Okhla as software hub of Delhi.</p>
